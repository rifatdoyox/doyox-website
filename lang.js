// Language switching function
function setLanguage(lang) {
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else if (el.tagName === 'SELECT') {
        // For select options, we handle separately via data-i18n-options
        // This is handled below
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });
  
  // Handle select options (for interest area dropdown)
  const interestSelect = document.querySelector('select[name="interest"]');
  if (interestSelect) {
    const options = interestSelect.querySelectorAll('option');
    const optionKeys = ['selectInterest', 'interestStartup', 'interestMicro', 'interestInvest', 'interestPartner', 'interestVentures'];
    options.forEach((opt, idx) => {
      if (idx < optionKeys.length) {
        opt.text = translations[lang][optionKeys[idx]];
      }
    });
  }
  
  // Handle placeholder for email input in newsletter
  const newsletterInput = document.querySelector('.newsletter-form input');
  if (newsletterInput) {
    newsletterInput.placeholder = translations[lang].yourEmail;
  }
  
  // Handle contact form inputs
  document.querySelector('input[name="name"]')?.setAttribute('placeholder', translations[lang].fullName);
  document.querySelector('input[name="email"]')?.setAttribute('placeholder', translations[lang].emailAddress);
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Save preference
  localStorage.setItem('doyox-lang', lang);
  
  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('doyox-lang') || 'en';
  setLanguage(savedLang);
  
  // Add click handlers to language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
});
