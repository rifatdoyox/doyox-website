function setLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang]?.[key]) el.innerHTML = translations[lang][key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang]?.[key]) el.placeholder = translations[lang][key];
  });
  document.querySelectorAll('select[data-i18n-select] option').forEach(opt => {
    const key = opt.getAttribute('data-i18n');
    if (key && translations[lang]?.[key]) opt.textContent = translations[lang][key];
  });
  document.documentElement.lang = lang;
  localStorage.setItem('doyox-lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.toggle('active', btn.dataset.lang === lang));
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('doyox-lang') || 'en';
  setLanguage(saved);
  document.querySelectorAll('.lang-btn').forEach(btn => btn.addEventListener('click', () => setLanguage(btn.dataset.lang)));
});
